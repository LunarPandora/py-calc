# Mari kita mulai mendefinisikan beberapa fungsi-fungsi perhitungan sederhana.
# Kita dapat memanfaatkan function untuk melakukan hal tersebut.

# Function pertambahan dengan 2 parameter, yakni x dan y. Operasi ini akan mengembalikan nilai x + y.
def pertambahan(x, y):
    return print("Hasil dari perhitungan tersebut adalah", int(x) + int(y))

# Function pengurangan dengan 2 parameter, yakni x dan y. Operasi ini akan mengembalikan nilai x - y.
def pengurangan(x, y):
    return print("Hasil dari perhitungan tersebut adalah", int(x) - int(y))

# Function perkalian dengan 2 parameter, yakni x dan y. Operasi ini akan mengembalikan nilai x * y.
def perkalian(x, y):
    return print("Hasil dari perhitungan tersebut adalah", int(x) * int(y))

# Function pembagian dengan 2 parameter, yakni x dan y. Operasi ini akan mengembalikan nilai x / y.
def pembagian(x, y):
    return print("Hasil dari perhitungan tersebut adalah", float(int(x) / int(y)))

# Function perpangkatan dengan 2 parameter, yakni x dan y. Operasi ini akan mengembalikan nilai x ** y.
def perpangkatan(x, y):
    return print("Hasil dari perhitungan tersebut adalah", int(x) ** int(y))